// Existing JavaScript functions to display events
document.addEventListener('DOMContentLoaded', () => {
    const eventsContainer = document.getElementById('events-container');

    if (!eventsContainer) {
        console.error('Events container not found.');
        return;
    }

    if (events.length === 0) {
        eventsContainer.innerHTML = '<p>No upcoming events currently listed. Please check back soon!</p>';
        return;
    }

    events.forEach(event => {
        const eventCard = document.createElement('div');
        eventCard.classList.add('event-card');

        // Add class for organization-specific title color
        let titleClass = '';
        switch(event.organization) {
            case "Edinburgh Steiner School": titleClass = 'title-steiner'; break;
            case "Tiphereth": titleClass = 'title-tiphereth'; break;
            case "Garvald": titleClass = 'title-garvald'; break;
            case "Columcille": titleClass = 'title-columcille'; break;
            case "Camphill Blair Drummond": titleClass = 'title-camphill-blair-drummond'; break;
            case "Camphill (Loch Arthur)": titleClass = 'title-camphill-loch-arthur'; break;
            case "Ruskin Mill (Fairhill Rise)": titleClass = 'title-ruskin-mill'; break;
            case "Edinburgh Christian Community": titleClass = 'title-christian-community'; break;
            default: titleClass = ''; // Default or fallback
        }

        eventCard.innerHTML = `
            <p class="event-org-name">${event.organization}</p>
            <h3 class="${titleClass}">
                ${event.eventUrl ? `<a href="${event.eventUrl}" target="_blank" rel="noopener noreferrer">${event.title}</a>` : event.title}
            </h3>
            <p><strong>Date:</strong> ${event.date}</p>
            ${event.time && event.time !== "Not specified" ? `<p><strong>Time:</strong> ${event.time}</p>` : ''}
            ${event.location ? `<p><strong>Location:</strong> ${event.location}</p>` : ''}
            ${event.cost && event.cost !== "Not specified" ? `<p><strong>Cost:</strong> ${event.cost}</p>` : ''}
            ${event.registration && event.registration !== "Not specified" ? `<p><strong>Registration:</strong> ${event.registration}</p>` : ''}
            ${event.contact && event.contact !== "Not specified" ? `<p><strong>Contact:</strong> ${event.contact}</p>` : ''}
            ${event.description ? `<p class="event-description">${event.description}</p>` : ''}
            ${event.eventUrl ? `<p class="more-info"><a href="${event.eventUrl}" target="_blank" rel="noopener noreferrer">More Information</a></p>` : ''}
        `;
        eventsContainer.appendChild(eventCard);
    });
});
